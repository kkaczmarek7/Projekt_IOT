import random
import time

print("Monitorowanie wilgotności i temperatury powietrza (symulacja)")

while True:
    temperature = round(random.uniform(-10.0, 30.0), 1)
    humidity = round(random.uniform(30.0, 90.0), 1)
    
    print(f"Temperatura: {temperature:.1f}°C  Wilgotność: {humidity:.1f}%")
    
    time.sleep(2)  # Odczyt co 2 sekundy
